					var address = data.Address;
					var html = "<b>" + name + "</b> <br>" + address;
          var marker = new google.maps.Marker({
            position: latLng,
			      map : map,
			      title : data.Name,
			      category : data.Type,
			      category2 : data.Company,
					  icon : iconAddress,
						//label: {
						//	text: label.icon,
						//	color: color.icon,
						//},
          });
					bindInfoWindow(marker,map,infoWindow,html);
